def load_sample_data(data_path, labels_path, num_samples=None):
    data = np.load(data_path, allow_pickle=True)
    labels = np.load(labels_path, allow_pickle=True)
    if num_samples is not None:
        indices = np.random.choice(len(data), num_samples, replace=False)
        data = data[indices]
        labels = labels[indices]
    return [torch.from_numpy(sample).float() for sample in data], labels

def evaluate_model(model, samples, true_labels, device):
    model.eval()
    correct_top1 = 0
    correct_top5 = 0
    predictions = []

    with torch.no_grad():
        for sample, true_label in tqdm(zip(samples, true_labels), total=len(samples)):
            poses = sample.unsqueeze(0).to(device)
            lengths = torch.tensor([sample.shape[0]], device=device)

            raw_prediction = model(poses, lengths)
            probabilities = torch.nn.functional.softmax(raw_prediction, dim=1)

            prediction = torch.argmax(probabilities).item()
            top5_indices = torch.topk(probabilities, 5)[1].cpu().numpy()

            predictions.append((prediction, top5_indices, true_label))

            if prediction == true_label:
                correct_top1 += 1
            if true_label in top5_indices:
                correct_top5 += 1

    return correct_top1 / len(samples), correct_top5 / len(samples), predictions

def analyze_predictions(predictions, label_to_meaning):
    class_accuracies = {}
    confusion_matrix = np.zeros((len(label_to_meaning), len(label_to_meaning)), dtype=int)

    for pred, top5, true in predictions:
        if true not in class_accuracies:
            class_accuracies[true] = {'correct': 0, 'total': 0}
        class_accuracies[true]['total'] += 1
        if pred == true:
            class_accuracies[true]['correct'] += 1

        confusion_matrix[true][pred] += 1

    # 打印每个类别的准确率
    print("\n各类别准确率:")
    for label, stats in class_accuracies.items():
        accuracy = stats['correct'] / stats['total']
        print(f"类别 {label} ({label_to_meaning[label]}): {accuracy:.2f} ({stats['correct']}/{stats['total']})")

    # 打印混淆矩阵（只打印非零元素）
    print("\n混淆矩阵 (只显示非零元素):")
    for i in range(confusion_matrix.shape[0]):
        for j in range(confusion_matrix.shape[1]):
            if confusion_matrix[i][j] > 0:
                print(f"真实: {i} ({label_to_meaning[i]}), 预测: {j} ({label_to_meaning[j]}), 次数: {confusion_matrix[i][j]}")

